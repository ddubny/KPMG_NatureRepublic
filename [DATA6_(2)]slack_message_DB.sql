CREATE TABLE position_code
(position_code int(20) NOT NULL,
position varchar(20),
PRIMARY KEY (position_code)
);

CREATE TABLE EMP
(emp_name varchar(20) NOT null,  
emp_phone varchar(12),
position char(10),
dept varchar(20),
task Varchar(20),
PRIMARY KEY (emp_name),
Unique KEY (emp_name)
);

CREATE TABLE slack_messages
( timeline DATETIME NOT NULL,
writer varchar(20), 
corp varchar(1000),
message varchar(10000),
PRIMARY KEY (timeline, writer),
foreign key(writer) REFERENCES EMP(emp_name)
);

